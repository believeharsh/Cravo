// Main Icon component that renders the correct icon based on props.
const Icon = ({
  name,
  size = 20,
  color = 'currentColor',
  className = '',
  ...props
}) => {
  const LucideIconComponent = iconMap[name.toLowerCase()];

  if (!LucideIconComponent) {
    if (process.env.NODE_ENV === 'development') {
      console.warn(
        `Icon "${name}" not found or not mapped. Please add it to the 'iconMap'.`
      );
    }
    return null;
  }

  // Rendering the specific Lucide icon component with the given props
  return (
    <LucideIconComponent
      size={size}
      color={color}
      className={className}
      {...props}
    />
  );
};

export default Icon;
